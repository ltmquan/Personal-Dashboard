package com.quanluu.dashboard.constants;

/**
 * Enum representing different types of academic content.
 *
 * Types:
 * - POST: General blog post or journal entry
 * - THEOREM: Mathematical theorem with proof
 * - DEFINITION: Concept or term definition
 * - PAPER: Research paper notes with citation
 * - PROBLEM: Problem statement and solution
 */
public enum ContentType {
    POST,
    THEOREM,
    DEFINITION,
    PAPER,
    PROBLEM
}
